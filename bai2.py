a = int(input("Nhập vào giá trị a: "))
b = int(input("Nhập vào giá trị b: "))

tong = a + b
hieu = a - b
tich = a * b
thuong = round(a / b , 3)
print(f"Tổng hai số là: {tong}")
print(f"Hiệu hai số là: {hieu}")
print(f"Tích hai số là: {tich}")
print(f"Thương hai số là: {thuong}")